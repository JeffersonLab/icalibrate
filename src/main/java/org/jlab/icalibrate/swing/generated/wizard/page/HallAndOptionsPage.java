    @Override
    public void leave() throws ValidationException {
        Hall hall = Hall.A;
        
        if(hallCRadioButton.isSelected()) {
            hall = Hall.C;
        } else if(hallDRadioButton.isSelected()) {
            hall = Hall.D;
        }
        
        CreateNewDatasetParameters params = getParameters();
        
        params.setHall(hall);
        
        int minAttenuator = (Integer)minAttenuatorInput.getValue();
        params.setMinAttenuator(minAttenuator);
        
        int maxAttenuator = (Integer)maxAttenuatorInput.getValue();
        params.setMaxAttenuator(maxAttenuator);
        
        int numberOfSteps = (Integer)numberOfStepsInput.getValue();
        params.setNumberOfSteps(numberOfSteps);
        
        int settleSeconds = (Integer)settleSecondsInput.getValue();
        params.setSettleSeconds(settleSeconds);
        
        int samplesPerStep = (Integer)samplesPerStepInput.getValue();
        params.setSamplesPerStep(samplesPerStep);
        
        params.checkOptionValidity();
    }    
    
    @Override
    public void reset() {
        hallARadioButton.setSelected(true);
        minAttenuatorInput.setValue(0);
        maxAttenuatorInput.setValue(100);
        numberOfStepsInput.setValue(10);
        settleSecondsInput.setValue(2);
        samplesPerStepInput.setValue(30);
    }
